    void init() {
        setLocationRelativeTo(null);
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }

    public boolean checkForm() {
        if (txtMaCD.getText().equals("")) {
            MsgBox.alert(this, "Mã chuyên đề không được để trống");
            return false;
        }
        if (txtTenCD.getText().equals("")) {
            MsgBox.alert(this, "Tên chuyên đề không được để trống");
            return false;
        }
        if (txtThoiLuong.getText().equals("")) {
            MsgBox.alert(this, "Thời lượng không được để trống");
            return false;
        }
//        String reThoiLuong = "\\d{1, 9}";
//        if (!txtThoiLuong.getText().equals(reThoiLuong)) {
//            MsgBox.alert(this, "Thời lượng phải là số!");
//            return false;
//        }
        if (txtHocPhi.getText().equals("")) {
            MsgBox.alert(this, "Học phí chuyên đề không được để trống");
            return false;
        }
//        String reHocPhi = "\\d{1,50}";
//        if (!txtHocPhi.getText().matches(reHocPhi)) {
//            MsgBox.alert(this, "Học phí phải là số!");
//            return false;
//        }
        return true;
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblGridView.getModel();
        model.setRowCount(0);
        try {
            List<ChuyenDe> list = dao.selectAll();
            for (ChuyenDe cd : list) {
                Object[] row = {
                    cd.getMaCD(),
                    cd.getTenCD(),
                    cd.getHocPhi(),
                    cd.getThoiLuong(),
                    cd.getHinh()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu");
        }
    }

    void setForm(ChuyenDe cd) {
        txtMaCD.setText(cd.getMaCD());
        txtTenCD.setText(cd.getTenCD());
        txtThoiLuong.setText(String.valueOf(cd.getThoiLuong()));
        txtHocPhi.setText(String.valueOf(cd.getHocPhi()));
        txtMoTa.setText(cd.getMoTa());
        if (cd.getHinh() != null) {
            lblHinhh.setToolTipText(cd.getHinh());
            lblHinhh.setIcon(XImage.read(cd.getHinh()));
        }
    }

    ChuyenDe getForm() {
        ChuyenDe cd = new ChuyenDe();
        cd.setMaCD(txtMaCD.getText());
        cd.setTenCD(txtTenCD.getText());
        cd.setThoiLuong(Integer.valueOf(txtThoiLuong.getText()));
        cd.setHocPhi(Double.valueOf(txtHocPhi.getText()));
        cd.setMoTa(txtMoTa.getText());
        cd.setHinh(lblHinhh.getToolTipText());
        return cd;
    }

    void insert() {
        ChuyenDe model = getForm();
        try {
            dao.insert(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thiếu hoặc trùng mã cd,Thêm mới thất bại!");
//            e.printStackTrace();
        }
    }

    void update() {
        ChuyenDe model = getForm();
        try {
            dao.update(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!");
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyền xóa chuyên đề!");
        } else {
            if (MsgBox.confirm(this, "Bạn có muốn xóa hay không?")) {
                String macd = txtMaCD.getText();
                try {
                    dao.delete(macd);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                    //e.printStackTrace();
                }
            }
        }
    }

    void clearForm() {
        this.setForm(new ChuyenDe());
        this.row = -1;
        this.updateStatus();
    }

    void edit() {
        String macd = (String) tblGridView.getValueAt(this.row, 0);
        ChuyenDe cd = dao.selectById(macd);
        this.setForm(cd);
        this.updateStatus();
        tabs.setSelectedIndex(0);
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tblGridView.getRowCount() - 1) {//lonh hon vi tri cuoi
            this.row++;
            this.edit();
        }
    }

    void last() {
        this.row = tblGridView.getRowCount() - 1;
        this.edit();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblGridView.getRowCount() - 1);
        //trang thai form
        txtMaCD.setEditable(!edit);
        btnInsert.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void chonAnh() {
        JFileChooser fileChooser = new JFileChooser("E:\\FPT_Poly\\DuAnMau(UDPM)\\PD05298ProjectSample\\src\\main\\java\\images");
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            XImage.save(file); //Luu hinh vao Images
            ImageIcon icon = XImage.read(file.getName());//doc hinh tu logos
            lblHinhh.setIcon(icon);
            lblHinhh.setToolTipText(file.getName());//giu ten hinh trong tolltip
        }
    }

